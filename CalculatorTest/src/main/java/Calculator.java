public class Calculator {

    public int add(int a, int b) {
        return a + b;
//        return 15;
    }

    public int multiple(int a, int b) {
        return a * b;
    }

    public double divide(int a, int b) {
        return (double) a / b;
    }

    public int square(int a) {
        return a * a;
    }
}
