package JavaToJunior.part_1;

public class Char {

    public static void main(String[] args) {
        char x = 88;
        char x1 = 'X';
        char x2 = 65535;
        System.out.println("Значение переменной x = " + x);
        System.out.println("Значение переменной x = " + x1);

        System.out.println(x2);

        x1++;
        System.out.println("Теперь значение переменной x = " + x);
        System.out.println("А значение переменной x1 = " + x1);
    }
}
